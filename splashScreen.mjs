class SplashScreen {

    start() {
        animationInterval = setInterval (() => {
            this.update();
            this.draw();
        }, 500);
    }

    stop() {
        if (animationInterval) {
            clearInterval(animationInterval);
        }
    }
    update() {
        currentFrame = (currentFrame + 1) % frames.length;
        isDirty = true;
    }
    draw() {
        if (isDirty) {
            console.log(ANSI.CLEAR_SCREEN, ANSI.CURSOR_HOME);
            console.log(frames[currentFrame]);
            isDirty = false;
        }
    }
}

